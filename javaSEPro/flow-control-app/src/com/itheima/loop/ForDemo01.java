package com.itheima.loop;

public class ForDemo01 {
    public static void main(String[] args) {
        //目标：学会使用if语句
        //输出三次hello world
        int i;
        for(i=0;i<3;i++){
            System.out.println("hello world");
        }
        for(i=0;i<3;++i){
            System.out.println("hello world");
        }
    }
}
