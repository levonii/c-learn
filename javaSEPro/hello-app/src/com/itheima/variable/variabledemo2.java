package com.itheima.variable;

public class variabledemo2 {
    public static void main(String[] args) {
        //目标：学会基本数据类型，及其定义方法
        //1、整型
        byte a=50;
        short b=2;
        int c=100000;
        long d=10000000000000L;
        System.out.printf("%d %d %d %d\n",a,b,c,d);
        //2、浮点型
        float a1=2.66F;
        double b1=3.66666666666;
    }
}
